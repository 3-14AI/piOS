extern crate alloc;
use alloc::collections::BTreeMap;
use alloc::string::{String, ToString};
// use alloc::vec::Vec;
use crate::wasm::wasi::WasiCtx;
#[cfg(not(feature = "verus"))]
use crate::wasm::wasi_nn::{
    compute, get_output, init_execution_context, load, load_by_name, set_input,
};
use wasmi::{Engine, Func, Instance, Linker, Module, Store};

pub struct WasmComponentLinker {
    engine: Engine,
    modules: BTreeMap<String, Module>,
}

impl Default for WasmComponentLinker {
    fn default() -> Self {
        Self::new()
    }
}

impl WasmComponentLinker {
    pub fn new() -> Self {
        Self {
            engine: Engine::default(),
            modules: BTreeMap::new(),
        }
    }

    pub fn add_module(&mut self, name: &str, wasm_bytes: &[u8]) -> Result<(), wasmi::Error> {
        let module = Module::new(&self.engine, wasm_bytes)?;
        self.modules.insert(name.to_string(), module);
        Ok(())
    }

    pub fn link_and_run(&self, main_module_name: &str) -> Result<(), wasmi::Error> {
        let mut store = Store::new(&self.engine, WasiCtx::new());
        let mut linker = <Linker<WasiCtx>>::new(&self.engine);

        // Define WASI imports
        linker.define(
            "wasi_snapshot_preview1",
            "fd_write",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::fd_write),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "fd_read",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::fd_read),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "fd_close",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::fd_close),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "environ_get",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::environ_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "environ_sizes_get",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::environ_sizes_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "args_get",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::args_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "args_sizes_get",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::args_sizes_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "proc_exit",
            wasmi::Func::wrap(&mut store, crate::wasm::wasi::proc_exit),
        )?;

        #[cfg(not(feature = "verus"))]
        {
            linker.define("wasi_ephemeral_nn", "load", Func::wrap(&mut store, load))?;
            linker.define(
                "wasi_ephemeral_nn",
                "load_by_name",
                Func::wrap(&mut store, load_by_name),
            )?;
            linker.define(
                "wasi_ephemeral_nn",
                "init_execution_context",
                Func::wrap(&mut store, init_execution_context),
            )?;
            linker.define(
                "wasi_ephemeral_nn",
                "set_input",
                Func::wrap(&mut store, set_input),
            )?;
            linker.define(
                "wasi_ephemeral_nn",
                "compute",
                Func::wrap(&mut store, compute),
            )?;
            linker.define(
                "wasi_ephemeral_nn",
                "get_output",
                Func::wrap(&mut store, get_output),
            )?;
        }

        // First pass: instantiate all dependencies
        for (name, module) in &self.modules {
            if name != main_module_name {
                let instance = linker.instantiate_and_start(&mut store, module)?;
                linker.instance(&mut store, name, instance)?;
            }
        }

        // Second pass: instantiate main module
        if let Some(main_module) = self.modules.get(main_module_name) {
            let instance = linker.instantiate_and_start(&mut store, main_module)?;

            if let Some(wasmi::Extern::Func(main)) = instance.get_export(&store, "main") {
                let typed_main = main.typed::<(), ()>(&store)?;
                typed_main.call(&mut store, ())?;
            }
            Ok(())
        } else {
            Err(wasmi::Error::new("Main module not found"))
        }
    }
}

