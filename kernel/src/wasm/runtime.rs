extern crate alloc;

use crate::wasm::wasi::{
    args_get, args_sizes_get, environ_get, environ_sizes_get, fd_close, fd_read, fd_write,
    proc_exit, WasiCtx,
};
use crate::wasm::wasi_nn::{
    compute, get_output, init_execution_context, load, load_by_name, set_input,
};
use alloc::vec::Vec;
use wasmi::{Engine, Extern, Func, Linker, Module, Store};

pub struct WasmRuntime {
    engine: Engine,
}

impl Default for WasmRuntime {
    fn default() -> Self {
        Self::new()
    }
}

impl WasmRuntime {
    pub fn new() -> Self {
        let engine = Engine::default();
        Self { engine }
    }

    pub fn run(&self, wasm_bytes: &[u8]) -> Result<(), wasmi::Error> {
        let module = Module::new(&self.engine, wasm_bytes)?;

        type HostState = WasiCtx;
        let mut store = Store::new(&self.engine, WasiCtx::new());

        let mut linker = <Linker<HostState>>::new(&self.engine);
        linker.define(
            "wasi_snapshot_preview1",
            "fd_write",
            Func::wrap(&mut store, fd_write),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "fd_read",
            Func::wrap(&mut store, fd_read),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "fd_close",
            Func::wrap(&mut store, fd_close),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "environ_get",
            Func::wrap(&mut store, environ_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "environ_sizes_get",
            Func::wrap(&mut store, environ_sizes_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "args_get",
            Func::wrap(&mut store, args_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "args_sizes_get",
            Func::wrap(&mut store, args_sizes_get),
        )?;
        linker.define(
            "wasi_snapshot_preview1",
            "proc_exit",
            Func::wrap(&mut store, proc_exit),
        )?;

        linker.define("wasi_ephemeral_nn", "load", Func::wrap(&mut store, load))?;
        linker.define(
            "wasi_ephemeral_nn",
            "load_by_name",
            Func::wrap(&mut store, load_by_name),
        )?;
        linker.define(
            "wasi_ephemeral_nn",
            "init_execution_context",
            Func::wrap(&mut store, init_execution_context),
        )?;
        linker.define(
            "wasi_ephemeral_nn",
            "set_input",
            Func::wrap(&mut store, set_input),
        )?;
        linker.define(
            "wasi_ephemeral_nn",
            "compute",
            Func::wrap(&mut store, compute),
        )?;
        linker.define(
            "wasi_ephemeral_nn",
            "get_output",
            Func::wrap(&mut store, get_output),
        )?;

        // For backward compatibility with the old test
        let _ = linker.define(
            "env",
            "hello",
            Func::wrap(
                &mut store,
                |_caller: wasmi::Caller<'_, HostState>, _param: i32| {},
            ),
        );

        let instance = linker.instantiate_and_start(&mut store, &module)?;

        if let Some(Extern::Func(main)) = instance.get_export(&mut store, "main") {
            let typed_main = main.typed::<(), ()>(&store)?;
            typed_main.call(&mut store, ())?;
        }

        Ok(())
    }
}

