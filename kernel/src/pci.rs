#[cfg(feature = "verus")]
verus! {
    /// PCI Device Configuration Header
    #[derive(Copy, Clone)]
    pub struct PciConfig {
        pub vendor_id: u16,
        pub device_id: u16,
        pub class_code: u8,
        pub subclass: u8,
        pub prog_if: u8,
        pub bus: u8,
        pub device: u8,
        pub function: u8,
    }

    /// Result of parsing PCI Config
    pub enum PciParseResult {
        Valid(PciConfig),
        InvalidVendor,
    }

    /// Verifiable PCI Bus Enumerator
    pub struct PciEnumerator {
        pub max_bus: u8,
    }

    impl PciEnumerator {
        pub fn new(max_bus: u8) -> (e: Self)
            ensures e.max_bus == max_bus
        {
            PciEnumerator { max_bus }
        }

        #[allow(clippy::too_many_arguments)]
        pub fn parse_config(&self, vendor_id: u16, device_id: u16, class_code: u8, subclass: u8, prog_if: u8, bus: u8, device: u8, function: u8) -> (res: PciParseResult)
            ensures
                vendor_id == 0xffff ==> res is InvalidVendor,
                vendor_id != 0xffff ==> (
                    res is Valid &&
                    res->Valid_0.vendor_id == vendor_id &&
                    res->Valid_0.device_id == device_id &&
                    res->Valid_0.class_code == class_code &&
                    res->Valid_0.subclass == subclass &&
                    res->Valid_0.prog_if == prog_if &&
                    res->Valid_0.bus == bus &&
                    res->Valid_0.device == device &&
                    res->Valid_0.function == function
                )
        {
            if vendor_id == 0xffff {
                PciParseResult::InvalidVendor
            } else {
                PciParseResult::Valid(PciConfig {
                    vendor_id,
                    device_id,
                    class_code,
                    subclass,
                    prog_if,
                    bus,
                    device,
                    function,
                })
            }
        }
    }
}

#[cfg(not(feature = "verus"))]
use core::arch::asm;

#[cfg(not(feature = "verus"))]
pub struct PciPortManager;

#[cfg(not(feature = "verus"))]
impl Default for PciPortManager {
    fn default() -> Self {
        Self::new()
    }
}

#[cfg(not(feature = "verus"))]
impl PciPortManager {
    pub fn new() -> Self {
        PciPortManager
    }

    /// # Safety
    /// Writing directly to arbitrary ports using `out` instruction is inherently unsafe.
    /// The caller must ensure that the targeted port correctly handles the value provided
    /// without violating memory bounds or triggering unstable device states.
    pub unsafe fn outl(
        &self,
        #[allow(unused_variables)] port: u16,
        #[allow(unused_variables)] value: u32,
    ) {
        #[cfg(target_arch = "x86_64")]
        asm!(
            "out dx, eax",
            in("dx") port,
            in("eax") value,
            options(nomem, nostack, preserves_flags)
        );
        #[cfg(not(target_arch = "x86_64"))]
        {}
    }

    /// # Safety
    /// Reading from arbitrary ports using `in` instruction is unsafe.
    /// The caller must verify that performing a read on the specific I/O port does not
    /// trigger undesired side effects (e.g., clearing hardware state inadvertently).
    pub unsafe fn inl(&self, #[allow(unused_variables)] port: u16) -> u32 {
        #[cfg(target_arch = "x86_64")]
        {
            let value: u32;
            asm!(
                "in eax, dx",
                out("eax") value,
                in("dx") port,
                options(nomem, nostack, preserves_flags)
            );
            value
        }
        #[cfg(not(target_arch = "x86_64"))]
        {
            0
        }
    }
}

#[cfg(not(feature = "verus"))]
#[derive(Copy, Clone, Debug, PartialEq)]
pub struct PciConfig {
    pub vendor_id: u16,
    pub device_id: u16,
    pub class_code: u8,
    pub subclass: u8,
    pub prog_if: u8,
    pub bus: u8,
    pub device: u8,
    pub function: u8,
}

#[cfg(not(feature = "verus"))]
#[derive(Debug, PartialEq)]
pub enum PciParseResult {
    Valid(PciConfig),
    InvalidVendor,
}

#[cfg(not(feature = "verus"))]
#[derive(Debug)]
pub struct PciEnumerator {
    pub max_bus: u8,
}

#[cfg(not(feature = "verus"))]
impl PciEnumerator {
    pub fn new(max_bus: u8) -> Self {
        PciEnumerator { max_bus }
    }

    #[cfg(not(test))]
    pub fn read_config_register(bus: u8, device: u8, function: u8, offset: u8) -> u32 {
        let address = 0x80000000
            | ((bus as u32) << 16)
            | ((device as u32) << 11)
            | ((function as u32) << 8)
            | (offset as u32 & 0xFC);

        unsafe {
            let port_manager = PciPortManager::new();
            port_manager.outl(0xCF8, address);
            port_manager.inl(0xCFC)
        }
    }

    #[cfg(test)]
    pub fn read_config_register(bus: u8, device: u8, function: u8, offset: u8) -> u32 {
        // Mock implementation for tests
        if bus == 0 && device == 0 && function == 0 {
            if offset == 0 {
                return 0x100e8086; // vendor 8086, device 100e
            }
            if offset == 8 {
                return 0x02000000; // class code 0x02
            }
            if offset == 12 {
                return 0x00000000; // single function
            }
        }
        if bus == 0 && device == 1 && function == 0 {
            if offset == 0 {
                return 0x10011af4; // vendor 1af4, device 1001
            }
            if offset == 8 {
                return 0x01000000; // class code 0x01
            }
            if offset == 12 {
                return 0x00800000; // multi-function
            }
        }
        if bus == 0 && device == 1 && function == 1 {
            if offset == 0 {
                return 0x10021af4; // vendor 1af4, device 1002
            }
            if offset == 8 {
                return 0x02000000; // class code 0x02
            }
            if offset == 12 {
                return 0x00000000; // multi-function flag only checked on func 0
            }
        }

        0xFFFFFFFF // Invalid vendor
    }

    pub fn scan_bus(&self) -> Vec<PciConfig> {
        let mut devices = Vec::new();
        for bus in 0..=self.max_bus {
            for device in 0..32 {
                for function in 0..8 {
                    let vendor_device = Self::read_config_register(bus, device, function, 0);
                    let vendor_id = (vendor_device & 0xFFFF) as u16;

                    if vendor_id == 0xFFFF {
                        if function == 0 {
                            break;
                        } else {
                            continue;
                        }
                    }

                    let device_id = (vendor_device >> 16) as u16;
                    let class_info = Self::read_config_register(bus, device, function, 8);
                    let prog_if = ((class_info >> 8) & 0xFF) as u8;
                    let subclass = ((class_info >> 16) & 0xFF) as u8;
                    let class_code = ((class_info >> 24) & 0xFF) as u8;

                    if let PciParseResult::Valid(config) = self.parse_config(
                        vendor_id, device_id, class_code, subclass, prog_if, bus, device, function,
                    ) {
                        devices.push(config);
                    }

                    // Check if multi-function device
                    if function == 0 {
                        let header_type =
                            (Self::read_config_register(bus, device, function, 12) >> 16) as u8;
                        if (header_type & 0x80) == 0 {
                            break;
                        }
                    }
                }
            }
        }
        devices
    }

    #[allow(clippy::type_complexity)]
    pub fn load_drivers(
        &self,
        linker: &mut crate::wasm::linker::WasmComponentLinker,
        devices: &[PciConfig],
        drivers_data: &[(&str, u16, u16, &[u8], &[u8], &[u8])],
    ) -> Result<Vec<alloc::string::String>, wasmi::Error> {
        let mut loaded = Vec::new();
        for config in devices {
            for (driver_name, vendor_id, device_id, wasm_bytes, signature, public_key) in
                drivers_data
            {
                if config.vendor_id == *vendor_id && config.device_id == *device_id {
                    if !crate::wasm::secure_boot::SecureBoot::verify_signature(
                        wasm_bytes, signature, public_key,
                    ) {
                        return Err(wasmi::Error::new(
                            "Secure Boot validation failed: invalid signature",
                        ));
                    }
                    let instance_name = alloc::format!(
                        "{}_{}_{}_{}",
                        driver_name,
                        config.bus,
                        config.device,
                        config.function
                    );
                    linker.add_module(&instance_name, wasm_bytes)?;
                    loaded.push(instance_name);
                }
            }
        }
        Ok(loaded)
    }

    #[allow(clippy::too_many_arguments)]
    pub fn parse_config(
        &self,
        vendor_id: u16,
        device_id: u16,
        class_code: u8,
        subclass: u8,
        prog_if: u8,
        bus: u8,
        device: u8,
        function: u8,
    ) -> PciParseResult {
        if vendor_id == 0xffff {
            PciParseResult::InvalidVendor
        } else {
            PciParseResult::Valid(PciConfig {
                vendor_id,
                device_id,
                class_code,
                subclass,
                prog_if,
                bus,
                device,
                function,
            })
        }
    }
}

